    numero = Number(prompt("Digite um numero: "))

    switch(numero){

        case 0:

            alert("O número é 0!")
            break

        case 5:

            alert("O número é 5!")
            break

        case 10:

            alert("O número é 10!")
            break

        default: 

            alert("O número é nada!")

    }

